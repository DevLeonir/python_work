age = 28

if age < 2:
    print("You are baby!")
elif age == 2 or age < 4:
    print("You are a child!")
elif age == 4 or age < 13:
    print("You are a boy!")
elif age == 13 or age < 20:
    print("You are a teenager!")
elif age == 20 or age < 65:
    print("You are an adult!")
else:
    print("You are an elder!")
